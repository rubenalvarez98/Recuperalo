print("-----3.Distancia----")

velocidad = float(input("Ingresa la velocidad en kilómetros por hora (Km/h): "))
tiempo = float(input("Ingresa el tiempo en horas (h): "))

velocidad_km_por_hora = velocidad

distancia = velocidad_km_por_hora * tiempo

print("La distancia recorrida es:", distancia, "kilómetros")
